// ATRIBUTOS = VARIABLES

package a.c.atributos;

public class Atributos {
	
	public static void main(String[] args) {
		
		//PROGRAMA QUE EXPLICA QUE SON LOS ATRIBUTOS
		//ATRIBUTO = VARIABLE
		//RESERVAR ESPACIO DE MEMORIA - EN RAM
		
		int x; //ATRIBUTO ENTERO
		double y; //ATRIBUTO DECIMAL
		char z; //ATRIBUTO - SOLO GUARDA UN CARACTER
		
		//SE INTRODUCE VALOR A LOS ATRIBUTOS
		x = 10;
		y = 10.50;
		z = 'a';
		
		System.out.println("El valor de x es " + x);
		System.out.println("El valor de y es " + y);
		System.out.println("El valor de x es " + x + " y el valor de y es " + y);
		System.out.println("El valor de z es " + z);
	}

}