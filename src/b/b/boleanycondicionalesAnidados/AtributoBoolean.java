package b.b.boleanycondicionalesAnidados;

public class AtributoBoolean {

	public static void main(String[] args) {
		
		boolean b = true;
		
		if(b) {
			System.out.println("Verdadero");
		}else {
			System.out.println("Falso");
		}
		
		if(!b) {
			System.out.println("Falso");
		}else {
			System.out.println("Verdadero");
		}
	}
}